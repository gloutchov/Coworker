window.i18n = {
  currentLanguage: "it",
  fallbackLanguage: "it",

  init: function() {
    const available = Object.keys(vocabulary);
    this.fallbackLanguage = available.includes("it") ? "it" : available[0] || "en";
    
    let preferredLanguage = this.fallbackLanguage;
    try {
      const savedLang = localStorage.getItem("preferredLanguage");
      if (savedLang && available.includes(savedLang)) {
        preferredLanguage = savedLang;
      }
    } catch {}
    
    const langToggle = document.getElementById("lang-toggle");
    if (langToggle) {
      langToggle.addEventListener("click", () => this.toggleLanguage());
    }
    this.setLanguage(preferredLanguage);
  },

  setLanguage: function(lang) {
    const available = Object.keys(vocabulary);
    if (!available.includes(lang)) {
      lang = this.fallbackLanguage;
    }
    this.currentLanguage = lang;
    document.documentElement.lang = lang;
    this.updateAllElements();
    this.updateIcon();
    
    try {
      localStorage.setItem("preferredLanguage", lang);
    } catch {}
    
    window.dispatchEvent(new CustomEvent("languagechange", { detail: { language: this.currentLanguage } }));
  },

  toggleLanguage: function() {
    this.setLanguage(this.currentLanguage === "it" ? "en" : this.fallbackLanguage);
  },

  hasKey: function(key) {
    if (!key) return false;
    const dictionary = vocabulary[this.currentLanguage] || {};
    const fallbackDictionary = vocabulary[this.fallbackLanguage] || {};
    return (
      typeof dictionary[key] === "string" ||
      typeof fallbackDictionary[key] === "string"
    );
  },

  translate: function(key, params = {}) {
    if (!key) return "";
    const dictionary = vocabulary[this.currentLanguage] || {};
    const fallbackDictionary = vocabulary[this.fallbackLanguage] || {};
    const template = dictionary[key] ?? fallbackDictionary[key]; // Do NOT return the key as a fallback

    if (typeof template !== "string") {
      // If the key is not found, return a debug message.
      return `[missing: ${key}]`;
    }

    // Simple param substitution
    return template.replace(/\{([^}]+)\}/g, (_, token) => {
      const value = params[token.trim()];
      return typeof value === "undefined" ? `{${token}}` : value;
    });
  },

  resolveText: function(input, params = {}) {
    if (!input) return "";
    if (typeof input === "object" && input.key) {
      return this.translate(input.key, input.params || params);
    }
    if (typeof input === "string") {
      const translated = this.translate(input, params);
      const fallbackDictionary = vocabulary[this.fallbackLanguage] || {};
      if (translated.startsWith('[missing:') && !fallbackDictionary.hasOwnProperty(input)) {
         return input.replace(/\{([^}]+)\}/g, (_, token) => {
            const value = params[token.trim()];
            return typeof value === "undefined" ? `{${token}}` : value;
         });
      }
      return translated;
    }
    return String(input);
  },

  applyToElement: function(el, key, params = {}, attr) {
    if (!el || !key) return;
    el.dataset.translateKey = key;
    if (attr) {
      el.dataset.translateAttr = attr;
    }
    if (params && Object.keys(params).length) {
      el.dataset.translateParams = JSON.stringify(params);
    } else {
      delete el.dataset.translateParams;
    }
    this.updateElement(el);
  },

  updateElement: function(el) {
    if (!el || !el.dataset.translateKey) return;
    const attr = el.dataset.translateAttr || "text";
    let params = {};
    if (el.dataset.translateParams) {
      try {
        params = JSON.parse(el.dataset.translateParams);
      } catch {}
    }
    const value = this.translate(el.dataset.translateKey, params);
    
    switch (attr) {
      case "text": el.textContent = value; break;
      case "html": el.innerHTML = value; break;
      case "placeholder": el.placeholder = value; break;
      case "value": el.value = value; break;
      default: el.setAttribute(attr, value);
    }
  },

  updateAllElements: function() {
    document.querySelectorAll("[data-translate-key]").forEach(el => this.updateElement(el));
  },

  updateIcon: function() {
    const langIcon = document.getElementById("lang-icon");
    if (!langIcon) return;
    const showEnglish = this.currentLanguage === "it";
    langIcon.src = showEnglish ? "gb.ico" : "it.ico";
    const tooltip = this.translate(showEnglish ? "languageIconEnglish" : "languageIconItalian");
    langIcon.alt = tooltip;
    langIcon.title = tooltip;
  }
};
